from concurrent.futures import ThreadPoolExecutor, as_completed


def download_one(task: str, episode: str, output_dir: Path) -> tuple[str, str, bool]:
    remote = f"{BASE}/{task}/success_episodes/{episode}/data/trajectory.hdf5"
    cmd = ["modelscope", "download", "--dataset", PART_REPO, remote, "--local_dir", str(output_dir)]
    ok = subprocess.run(cmd).returncode == 0
    return task, episode, ok


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--task", type=str, nargs="+", default=list(TASKS.keys()),
                        help=f"Tasks to download (default: all 3)")
    parser.add_argument("--max-episodes", type=int, default=50)
    parser.add_argument("--workers", type=int, default=3,
                        help="Parallel downloads (default 3)")
    parser.add_argument("--output", type=str, default="data/raw/robomind")
    args = parser.parse_args()

    output_dir = Path(args.output)

    # Build list of all (task, episode) to download
    jobs = []
    for task_name in args.task:
        all_eps = TASKS.get(task_name, [])
        episodes = all_eps[:args.max_episodes]
        if not episodes:
            print(f"\n=== {task_name}: no episodes configured ===")
            continue
        print(f"{task_name}: {len(episodes)} episodes")
        for ep in episodes:
            jobs.append((task_name, ep))

    total = len(jobs)
    print(f"\nTotal: {total} files, {args.workers} workers\n")

    total_ok = 0
    total_fail = 0
    with ThreadPoolExecutor(max_workers=args.workers) as ex:
        futs = {ex.submit(download_one, t, e, output_dir): (t, e) for t, e in jobs}
        for i, f in enumerate(as_completed(futs), 1):
            task, ep, ok = f.result()
            total_ok += 1 if ok else 0
            total_fail += 0 if ok else 1
            print(f"  [{i}/{total}] {task} {ep}: {'OK' if ok else 'FAIL'}")

    print(f"\n=== Complete: {total_ok} OK, {total_fail} FAIL ===")
    print(f"Convert: python scripts/convert_robomind.py --data-dir {output_dir} --fps 5 --output data/processed/metadata.json")


if __name__ == "__main__":
    main()
